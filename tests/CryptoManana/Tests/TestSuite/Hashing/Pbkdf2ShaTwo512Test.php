            '6D2ECBBBFB2E6DCD7056FAF9AF6AA06EAE594391DB983279A6BF27E0EB2286143AB0' .
            'C996F33CA4B667E945829EA693340F2831797324E5F31DF18ED171D18C97',
            $hasher->hashData('')
        );

        $hasher->setDigestFormat($hasher::DIGEST_OUTPUT_HEX_LOWER);
        $this->assertEquals($hasher::DIGEST_OUTPUT_HEX_LOWER, $hasher->getDigestFormat());

