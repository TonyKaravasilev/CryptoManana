        $this->assertEquals($hasher::SALTING_MODE_INFIX_INPUT, $hasher->getSaltingMode());

        $this->assertEquals(
            'D87E9948170CAC014F2E56533FB8CCA303301FE2F0BAEFC8FECA99A10EBE' .
            'B3926320E05C721DD0D895EACBCB9C2512C10B2E87FC09F769436A442AFEBC195583',
